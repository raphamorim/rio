#[inline]
fn pixel_color(value: u8) -> Option<[f32; 4]> {
    match value {
        1 => Some(BLACK),
        2 => Some(WHITE),
        3 => Some(GRAY),
        4 => Some(MID),
        5 => Some(DARK),
        _ => None,
    }
}

pub fn draw(sugarloaf: &mut Sugarloaf, mouse_x: f32, mouse_y: f32, scale: f32) {
    let px = PX;
    let base_x = mouse_x / scale;
    let base_y = mouse_y / scale;

    for (row, line) in HAND.iter().enumerate() {
        for (col, &value) in line.iter().enumerate() {
            if let Some(color) = pixel_color(value) {
                let x = base_x + col as f32 * px;
                let y = base_y + row as f32 * px;
                sugarloaf.rect(None, x, y, px, px, color, DEPTH, ORDER);
            }
        }
    }
}
